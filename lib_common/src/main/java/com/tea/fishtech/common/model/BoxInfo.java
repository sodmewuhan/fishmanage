package com.tea.fishtech.common.model;

public class BoxInfo {

    private Integer id;

    private String boxNumber;

    private String registerphone;

    private String boxsimnumber;

    private Integer userid;

    private String boxname;

    private String boxTypeId;

    private String boxTypeName;

    private Long fishPondId;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getBoxNumber() {
        return boxNumber;
    }

    public void setBoxNumber(String boxNumber) {
        this.boxNumber = boxNumber;
    }

    public String getRegisterphone() {
        return registerphone;
    }

    public void setRegisterphone(String registerphone) {
        this.registerphone = registerphone;
    }

    public String getBoxsimnumber() {
        return boxsimnumber;
    }

    public void setBoxsimnumber(String boxsimnumber) {
        this.boxsimnumber = boxsimnumber;
    }

    public Integer getUserid() {
        return userid;
    }

    public void setUserid(Integer userid) {
        this.userid = userid;
    }

    public String getBoxname() {
        return boxname;
    }

    public void setBoxname(String boxname) {
        this.boxname = boxname;
    }

    public String getBoxTypeId() {
        return boxTypeId;
    }

    public void setBoxTypeId(String boxTypeId) {
        this.boxTypeId = boxTypeId;
    }

    public String getBoxTypeName() {
        return boxTypeName;
    }

    public void setBoxTypeName(String boxTypeName) {
        this.boxTypeName = boxTypeName;
    }

    public Long getFishPondId() {
        return fishPondId;
    }

    public void setFishPondId(Long fishPondId) {
        this.fishPondId = fishPondId;
    }
}
